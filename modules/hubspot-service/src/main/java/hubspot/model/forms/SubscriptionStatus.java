package hubspot.model.forms;

public class SubscriptionStatus {
    String id;
    String name;
    String description;
    String status;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public String getStatus() {
        return status;
    }
}
